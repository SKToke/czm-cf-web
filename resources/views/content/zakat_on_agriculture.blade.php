    <p class="text-dark"><strong>Meaning</strong></p>
    <p>'Ushr is the obligatory charge (Zakat) on farm produce which is one-tenth for one kind of land and one-twentieth for others.</p>


    <p class="text-dark"><strong>Shari’ah Injunction</strong></p>
    <br>
    <p class="text-dark"><strong>The Qur’an says:</strong></p>
    <p class="quran-verse">“O Believers! Spend in Allah’s way the best portion of the wealth you have earned and of what We have produced for you from the earth.” (2: 267)</p>
    <br>
    <p class="text-dark"><strong>And elsewhere:</strong></p>
    <p>“And give away Allah’s due at the harvest time.” (6:141)</p>

    <p>The Commentators are agreed that this refers to ‘Ushr on farm produce. Thus giving away of ‘Ushr on produce of the land is obligatory and [the] Hadith also has confirmed the same. The Holy Prophet has said:</p>

    <p>
        <p>“It is obligatory to give away one-tenth of the farm produce if the land is irrigated by spring or rain or river water naturally, and one-half of one tenth if it is irrigated by drawing out water from the well, etc.”</p>
    </p>

    <p class="text-dark"><strong>Rate [Amounts required] of ‘Ushr</strong></p>
    <p>
        If the farm or garden is watered naturally by rain or spring water, or river or stream water, [a] tenth part of the produce is obligatory, and if the farm or garden is irrigated by artificial means like the well, tube well, canals, etc. twentieth part is obligatory.

        One must take out the tenth or twentieth part of all kinds of produce at the time of harvest : it is not lawful to do so later when a portion of the crop has already been consumed. Thus ‘Ushr has to be calculated and taken out from all kinds of corn, mustard grain, ground nuts, sugarcane, dates, dry fruits, etc., and also from fresh vegetables and fruits, like cucumber, carrot, turnip, melons, lemon, oranges, guava, mango, etc. 1 ‘Ushr is obligatory on honey as well. The Holy Prophet has said: “Give away tenth part of honey”. (Baithaqi)

        He has explained it thus:
        “One leather bag of honey out of every ten leather bags is obligatory”. (Tirmidhi)

        Hadrat Abu Siyadah has reported: “I went to the Holy Prophet and said, ‘O Prophet of Allah! I have kept bees whereupon he instructed me to give away the tenth part”.

        On the basis of these traditions, the Hanafites, the Ahl alHadith, Ahmad bin Hanba, Umar bin ‘Abdul ‘Aziz and others are agreed that one is subject to ‘Ushr. Imam Shafei in one of his sayings has expressed a similar opinion, and Ibn ‘Umar and Ibn ‘Abbas from among the Companions have also supported the same view.
    </p>

    <p class="text-dark"><strong>Regulations Concerning ‘Ushr</strong></p>
    <p>
        ‘Ushr has to be taken out from the total produce and after that other charges of agriculture in the way of ploughing, watering, supervision, etc. will be met.2. The produce becomes subject to ‘Ushr as soon as the crop becomes fit for use in any form, e.g. the gram, maize, mango etc. become sable even before they are ripe; ‘ushr therefore has to be calculated before the crop is put to private use in any way.3. If a person sells away a crop or garden before the fruit is ripe, ‘Ushr will be the responsibility of the buyer, but if it is sold after the fruit has become ripe, ‘Ushr will be paid by the seller.4. Payment of ‘ushr is the responsibility of the cultivator of the land whether he has taken it on lease of borrowed it for a short period.5. If two persons are jointly cultivating a piece of land, both will have to share the ‘Ushr whether the expenses of seed, etc. have been borne buy one or both.6. There is no condition of Nisab for ‘Ushr to be obligatory : ‘Ushr is obligatory irrespective of the quantity of produce, unless it is in negligible quantity, e.g., 2 kilos or so.7. There is no condition of the passage of a year either : each crop is subject to ‘Ushr, whether it is harvested once a year or twice a year.8. ‘Ushr is levyable on the produce of lands owned by minors and insane persons.9. Waaf lands are also subject to ‘ushr, which will be paid away by the cultivator.10. If a piece of land is irrigated naturally as well as by artificial means, this fact will be taken into account while calculating ‘ushr. If the land has been irrigated naturally more often, ‘Ushr will be one-tenth , otherwise one-twentieth.11. ‘Ushr may be paid out in kind or in cash of equivalent value.
        12. Lands in possession of the Muslims living in non-Muslim states ae subject to ‘Ushr. (Fatawa Deoband, Vol. III, p.18)

        13. ‘Ushr is not annulled by the payment of land revenue.

        14. The heads of expenditure of ‘Ushr are the same as of Zakat.
    </p>

    <p class="text-dark"><strong>The Hidden Treasure</strong></p>
    <p>
        This may be minerals or some other treasure hidden in the earth. Regulations concerning it are briefly as follows:
    </p>
    <ol>
        <li>According to Hadith, one-fifth of a treasure, which is unearthed, belongs to the Public Treasury (Bait-ul-Mal).</li>
        <li>In case of minerals like iron, silver, gold, tin or sulphur, etc. one fifth will be handed in to the Public Treasury and four-fifth will be handed in to the Public Treasury and four-fifths will be retained by the mine owner.</li>
        <li>There is no share of the Bait-ul-Mal in those things which do not melt in the fire, e.g. diamonds, gems, etc. Similarly minerals in liquid state like petroleum, mercury, etc. are exempt from ‘Ushr duty.</li>

    </ol>

    <p class="text-dark"><strong>NOTES</strong></p>
    <p>
        According to some jurists, ‘ushr is not obligatory on those vegetables and fruits which are not stored, but if the grower sells them in the market, he will have to pay Zakat as on other trade goods if he trade capital amounts up to the Nisab limit of 200 dirhams or more in the beginning and the end of the trade.

        Imam Malik and Hadrat Sufyan opine that there is no ‘Ushr on honey and Imam Shafe’ also has supported this view.

        According to Imam Bukhari, no Hadith relating to Zakat on honey is authentic. Baihaqi has reported that a person brought his ‘Ushr of honey before he Holy Prophet and requested that arrangements be made for the protection of the Siblah forest and the Holy Prophet made the necessary arrangements. In the Caliphate of Hadrat ‘Umar, Sufyan bin Wahab, an official, sought has guidance and direction. Hadrat ‘Umar wrote him back, saying : “If the people give you that which they used to give to the Holy Prophet, you should take it, and make arrangements for the protection of the Siblah forest, otherwise honey prroduced by the bee is just like the rain water which may be used freely by an body”.

        Maulana Maududi has expressed the view that honey should be exempted from Zakat, but if it is made an article of trade, it should be made subject to Zakat just like other trade goods.

        This is the view of Imam Abu Hanifah. According to his celebrated pupils and Imam Shafi, the minimum quantity for ‘Ushr to be obligatory is five Wasaqs (app. 2000 lb.) The Ahl al-Hadith scholars concur in this view. They base their opinion on this Hadith :

        “There is no ‘Ushr on less than five Wasaqs of produce.”

        Imam Ahmad bin Hanbal holds the view that all minerals, whether in solid or liquid state, are subject to Zakat 2.5 per cent , provided their value reaches Nisab and they ae in private ownership. In the time of Hadrat ‘Umar bin ‘Abdul Aziz this was the actual practice of the state.
    </p>


    <p class="text-dark"><strong>(Al-Mughni, II,p.58)</strong></p>

