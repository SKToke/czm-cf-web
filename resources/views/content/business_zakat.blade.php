        <h4 class="text-dark"><strong>2. Conditions Governing Business Zakat</strong></h4>

        <ol>
            <li><p><strong class="text-dark">MUSLIM</strong><br>All business entities owned by Muslims are obligated to pay Zakat upon it. Businesses with mixed ownership (i.e. 80% equity ownership of Muslims), only the part owned by Muslims is obligated to be paid upon it.</p></li>
            <li><p><strong class="text-dark">ASSETS OR SERVICES THAT ARE HALAL (PERMISSIBLE BY SARIAH)</strong><br>Business assets must be from Halal sources and the business activities too must come from Halal activities or services. All business properties that are Haram either from physical aspect side (‘ain) or from activities that are Haram such as usury are excluded in the calculation of Zakat.</p></li>
            <li><p><strong class="text-dark">NISAB (MINIMUM AMOUNT)</strong><br>The nisab of business assets is the equivalent of 85 grams of gold (20 mithqal). When the value of the business assets are the same or exceeds nisab then Zakat is imposed on the stated assets.</p></li>
            <li><p><strong class="text-dark">HAUL (MINIMUM TIME PERIOD)</strong><br>Value of the assets calculated from the start of business until the end is known as haul. The haul period is calculated from the date the business started. For example haul for business is the financial year ending June or December.</p></li>
            <li><p><strong class="text-dark">PRODUCTIVE ASSETS</strong><br>Assets eligible for Zakat must be assets that are productive and can be developed or has the potential to develop which means that it is able to generate income and profit.</p></li>
            <li><p><strong class="text-dark">FULL OWNERSHIP</strong><br>Owner has the right to manage his assets including selling, gifting it or letting out the said asset. Physical ownership (hiyazah) and absolute management ownership (tasarruf) must also exist simultaneously.</p></li>
            <li><p><strong class="text-dark">THE SOURCE OF BUSINESS ASSETS MUST BE FROM BUYING OR EXCHANGING</strong><br>Business assets must be obtained legally. Income from charity is also calculated as business assets and is subject to Zakat payment.</p></li>
            <li><p><strong class="text-dark">INTENTIONS (NIAT) TO DO BUSINESS</strong><br>Owner must have intention to do business (i.e. obtain profit) during ownership of trading goods (urud tijarah).</p></li>
            <li><p><strong class="text-dark">BUSINESS ASSETS ARE NOT FOR PERSONAL USE</strong><br>Assets acquired and owned to be used personally is not subject to business Zakat but is subject to other types of Zakat such as Zakat on earned income (for individual).</p></li>
        </ol>

        <h4 class="text-dark"><strong>3. Assessment Method</strong></h4>

        <p>Business Zakat calculation takes into account the business’s Current Asset position, deducted over several Current Asset and Current Liability items.</p>

        <p class="text-dark"><strong>Zakat = (Current Asset + Adjustment in Current Assets + Adjustment in Current Liability) X Muslim Share Ownership X 2.5%</strong></p>

        <p>Although this method looks straight to the scope of Current Asset and Current Liability, indirectly, it also takes into account other scopes in business, for example, financing scope (equity and loan) and the scope of Fixed Assets used in the business. This is based on the following accounting equation:</p>

        <p class="text-dark"><strong>Current Asset + Current Liability = Owners’ Equity + Long-term Liability – Fixed Asset - Non-Current Asset</strong></p>

        <p>The above equation is derived from the original equation which is:<br> Assets = Liabilities + Equity</p>

        <p><strong class="text-dark">ZAKAT RATE</strong> <br> 2.5% on assets eligible for Zakat, subject to nisab.</p>

